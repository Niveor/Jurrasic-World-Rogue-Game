package game;

/**
 * the enum that stated the status of flying of the dinosaur
 */
public enum FlyingStatus {
    /**
     * the dinosaur can fly and has enough fuel
     */
    CAN_FLY,
    /**
     * the dinosaur is on a tree
     */
    ON_TREE,
    /**
     *  the dinosaur is out of fuel and is currently walking
     */
    WALKING
}
